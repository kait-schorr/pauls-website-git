        <p>I have called San Antonio TX home for the last twenty years.  I grew up in El Paso TX and I became enamored with the State of New Mexico during my youth.  I was attracted to New Mexico’s culture, architecture, and geography.  I fully understand the allure of New Mexico that prompted Georgia O’Keeffe to travel there and make it her home.  The overwhelming majority of my photographic prints feature landscapes and unique architectural structures in Texas and New Mexico.</p>  

        <p>The first time I saw a photograph that was printed on aluminum was during a trip I made to Santa Fe NM in 2011.  The print was a black and white image in which the grey portions of the image allowed the underlying aluminum to shine through.  The image took on a different appearance as the light struck it from different angles.  After returning to Texas, I started investigating the process of printing on aluminum, for both black and white and color images.  Color images on aluminum also produce remarkable photographic prints.  The colors are vivid and appear to spring forth from the aluminum panel.</p>

        <p>The result of my exploration into using aluminum as a print medium is Alumages by Paul™.  I print exclusively on aluminum. </p>

        <p>My prints are made using a dye-sublimation printing process to reproduce images onto aluminum panels.  For a more technical explanation as to the process, the following link will take you to a description of the <a href="printing-process.php">Printing Process</a>.  In addition to the richness and beauty of the images, they are light weight, fade and scratch resistant, and they do not require framing.</p>

